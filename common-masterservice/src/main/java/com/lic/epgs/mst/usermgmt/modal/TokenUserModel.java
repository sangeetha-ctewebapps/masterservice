package com.lic.epgs.mst.usermgmt.modal;

public class TokenUserModel {
	
	private String client_id;
	
	private String token;
	
	private String client_secret;
	
	private String username;
	
  private String Postman_Token;
	
	private String Host;
	
	private String User_Agent;
	
	private String Accept;
	
	private String Accept_Encoding;
	
	private String Connection;

	public String getClient_id() {
		return client_id;
	}

	public void setClient_id(String client_id) {
		this.client_id = client_id;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public String getClient_secret() {
		return client_secret;
	}

	public void setClient_secret(String client_secret) {
		this.client_secret = client_secret;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPostman_Token() {
		return Postman_Token;
	}

	public void setPostman_Token(String postman_Token) {
		Postman_Token = postman_Token;
	}

	public String getHost() {
		return Host;
	}

	public void setHost(String host) {
		Host = host;
	}

	public String getUser_Agent() {
		return User_Agent;
	}

	public void setUser_Agent(String user_Agent) {
		User_Agent = user_Agent;
	}

	public String getAccept() {
		return Accept;
	}

	public void setAccept(String accept) {
		Accept = accept;
	}

	public String getAccept_Encoding() {
		return Accept_Encoding;
	}

	public void setAccept_Encoding(String accept_Encoding) {
		Accept_Encoding = accept_Encoding;
	}

	public String getConnection() {
		return Connection;
	}

	public void setConnection(String connection) {
		Connection = connection;
	}

	
}
