package com.lic.epgs.mst.usermgmt.modal;

public class FunctionNameDesignationModel {
	
	
	private long cofinlimitId;

	private String functionName;

	private String designation;

	private String isActive;
	
	private String isDeleted;
	
	private String srNumber;
	
	private String emailId;

	public long getCofinlimitId() {
		return cofinlimitId;
	}

	public void setCofinlimitId(long cofinlimitId) {
		this.cofinlimitId = cofinlimitId;
	}

	public String getFunctionName() {
		return functionName;
	}

	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}

	public String getDesignation() {
		return designation;
	}

	public void setDesignation(String designation) {
		this.designation = designation;
	}

	public String getIsActive() {
		return isActive;
	}

	public void setIsActive(String isActive) {
		this.isActive = isActive;
	}

	public String getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(String isDeleted) {
		this.isDeleted = isDeleted;
	}

	public String getSrNumber() {
		return srNumber;
	}

	public void setSrNumber(String srNumber) {
		this.srNumber = srNumber;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	
	
	

}
